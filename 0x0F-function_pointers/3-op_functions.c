#include "3-calc.h"
/**
 * op_add - sum of two numbers.
 *
 * @a: 1st integer.
 * @b: 2nd integer.
 * Return: sum.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference of two numbers.
 *
 * @a: 1st integer.
 * @b: 2nd integer.
 * Return: difference.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product of two numbers.
 *
 * @a: 1st integer.
 * @b: 2nd integer.
 * Return: product.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of two numbers.
 *
 * @a: 1st integer.
 * @b: 2nd integer.
 * Return: division.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of two numbers.
 *
 * @a: 1st integer.
 * @b: 2nd integer.
 * Return: remainder.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
